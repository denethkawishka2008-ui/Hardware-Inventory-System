    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
 String searchData = txtsearch.getText().trim();
    String qtyText = txtqty.getText().trim();
    
  
    if (searchData.isEmpty() || qtyText.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter Item and Quantity!");
        return;
    }
    
    try {
        int qty = Integer.parseInt(qtyText);
        
        if (qty <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Quantity must be greater than 0!");
            return;
        }
        
        java.sql.Connection con = com.mycompany.inventorymanagementsystem.db.mycon();
        
      
        String sql = "SELECT item_code, item_name, selling_price, qty FROM items WHERE item_code=? OR barcode=?";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, searchData);
        pst.setString(2, searchData);
        
        java.sql.ResultSet rs = pst.executeQuery();
        
        if (rs.next()) {
            String code = rs.getString("item_code");
            String name = rs.getString("item_name");
            double price = rs.getDouble("selling_price");
            int availableQty = rs.getInt("qty");
            
   
            if (qty > availableQty) {
                javax.swing.JOptionPane.showMessageDialog(this, "Sorry! Only " + availableQty + " items available in Stock.");
                return;
            }
            
            double total = price * qty;
            javax.swing.table.DefaultTableModel dt = (javax.swing.table.DefaultTableModel) tblbill.getModel();
            
            boolean isExist = false;
            int existingRow = -1;
            
           
            for (int i = 0; i < dt.getRowCount(); i++) {
                if (dt.getValueAt(i, 0) != null) { 
                    if (dt.getValueAt(i, 0).toString().equals(code)) {
                        isExist = true;
                        existingRow = i;
                        break;
                    }
                }
            }
            
            if (isExist) {

                int oldQty = Integer.parseInt(dt.getValueAt(existingRow, 3).toString());
                int newQty = oldQty + qty;
                
                
                if (newQty > availableQty) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Cannot add more! Stock limit exceeded.");
                    return;
                }
                
                double newTotal = price * newQty;
                dt.setValueAt(newQty, existingRow, 3); 
                dt.setValueAt(newTotal, existingRow, 4); 
            } else {
              
                java.util.Vector v = new java.util.Vector();
                v.add(code);      
                v.add(name);      
                v.add(price);     
                v.add(qty);       
                v.add(total);     
                dt.addRow(v);
            }
            
        
            String updateSql = "UPDATE items SET qty = qty - ? WHERE item_code = ?";
            java.sql.PreparedStatement updatePst = con.prepareStatement(updateSql);
            updatePst.setInt(1, qty); 
            updatePst.setString(2, code);
            updatePst.executeUpdate(); 
            
           
            updateGrandTotal();
            
            
            txtsearch.setText("");
            txtqty.setText("");
            txtsearch.requestFocus(); 
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Item Error! Please search again.");
        }
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Quantity!");
    } catch (Exception e) {
        System.out.println("Add to Cart Error: " + e.getMessage());
    }
// TODO add your handling code here:
    }//GEN-LAST:event_btnAddToCartActionPerformed

