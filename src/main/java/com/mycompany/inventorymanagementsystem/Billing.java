    private void txtsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsearchKeyReleased
         String text = txtsearch.getText().trim();
    
  
    if (text.length() < 2) {
        searchPopup.setVisible(false);
        return;
    }
    
  
    if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER || 
        evt.getKeyCode() == java.awt.event.KeyEvent.VK_DOWN || 
        evt.getKeyCode() == java.awt.event.KeyEvent.VK_UP) {
        return;
    }
    
    try {
        java.sql.Connection con = com.mycompany.inventorymanagementsystem.db.mycon();
        
        
        String sql = "SELECT item_code, item_name, selling_price FROM items WHERE item_name LIKE ? OR barcode LIKE ? LIMIT 10";
        java.sql.PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, "%" + text + "%");
        pst.setString(2, "%" + text + "%");
        
        java.sql.ResultSet rs = pst.executeQuery();
        
        javax.swing.DefaultListModel<String> listModel = new javax.swing.DefaultListModel<>();
        itemCodesList.clear();
        
        boolean hasData = false;
        while (rs.next()) {
            hasData = true;
            String code = rs.getString("item_code");
            String name = rs.getString("item_name");
            double price = rs.getDouble("selling_price");
            
            
            listModel.addElement(name + "   |   Rs." + price);
            itemCodesList.add(code); }
        if (hasData) {
            suggestionList.setModel(listModel);
            
          
            suggestionList.setFixedCellHeight(25);
            searchPopup.setPopupSize(txtsearch.getWidth(), 150); 
            
           
            searchPopup.show(txtsearch, 0, txtsearch.getHeight());
            txtsearch.requestFocus(); // Focus එක සර්ච් බොක්ස් එකේම තියාගන්නවා
        } else {
            searchPopup.setVisible(false);
        }
        
    } catch (Exception e) {
        System.out.println("Live Search Error: " + e.getMessage());
    }    // TODO add your handling code here:
    }//GEN-LAST:event_txtsearchKeyReleased

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
int selectedRow = tblbill.getSelectedRow();
    
   
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select an item from the table to update!");
        return;
    }
    
   
    String qtyText = txtqty.getText().trim();
    if (qtyText.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid Quantity!");
        return;
    }
    
    try {
        int newQty = Integer.parseInt(qtyText);
        if (newQty <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Quantity must be greater than 0!");
            return;
        }
        
        
        double price = Double.parseDouble(tblbill.getValueAt(selectedRow, 2).toString());
        
       
        double newTotal = price * newQty;
        
      
        tblbill.setValueAt(newQty, selectedRow, 3); 
        tblbill.setValueAt(newTotal, selectedRow, 4); 
        
       
        updateGrandTotal();
        
     
        txtsearch.setText("");
        txtqty.setText("");
        tblbill.clearSelection(); 
        txtsearch.requestFocus();
        
        javax.swing.JOptionPane.showMessageDialog(this, "Item quantity updated successfully!");
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Quantity!");
    } catch (Exception e) {
        System.out.println("Update Error: " + e.getMessage());
    }
    }//GEN-LAST:event_updateActionPerformed

