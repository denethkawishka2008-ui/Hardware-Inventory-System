    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
         String id = txtid.getText();
        String name = txtcompanyname.getText();
        String phone = txtphonenumber.getText();
        String email = txtemail.getText();
        
        if(id.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a Supplier from Table to update!");
            return;
        }
        if(name.isEmpty() || phone.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Company Name and Phone cannot be empty!");
            return;
        }

        try {
            java.sql.Connection con = db.mycon();
            java.sql.PreparedStatement pst = con.prepareStatement(
                "UPDATE suppliers SET company_name=?, phone=?, email=? WHERE supplier_id=?"
            );
            pst.setString(1, name);
            pst.setString(2, phone);
            pst.setString(3, email);
            pst.setString(4, id); 
            
            int rows = pst.executeUpdate();
            if(rows > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Supplier Updated Successfully!");
                tb_load();                     
                btnclearActionPerformed(null); 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Supplier ID not found!");
            }
        } catch (java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnupdateActionPerformed

