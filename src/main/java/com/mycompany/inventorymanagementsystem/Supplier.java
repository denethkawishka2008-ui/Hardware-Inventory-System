    public void auto_id() {
    try {
        // db class එක හරහා database එකට connect වීම
        java.sql.Connection con = db.mycon(); 
        java.sql.Statement s = con.createStatement();
        
        // suppliers table එකේ දැනට තියෙන ලොකුම ID එක query කරලා ගන්නවා
        java.sql.ResultSet rs = s.executeQuery("SELECT MAX(supplier_id) FROM suppliers");
        
        if (rs.next() && rs.getString(1) != null) {
            String maxId = rs.getString(1); // උදාහරණයක් ලෙස: "S1001"
            
            // "S" අකුර අතහැර ඉතිරි අංක කොටස (1001) පමණක් වෙන් කර ගැනීම
            int idNum = Integer.parseInt(maxId.substring(1)); 
            idNum++; // අංකය 1කින් වැඩි කිරීම (1001 -> 1002)
            
            // නැවතත් "S" අකුර සමඟ අලුත් අංකය එකතු කර text field එකට දැමීම
            txtid.setText("S" + idNum);
        } else {
            // Database එකේ කිසිම කෙනෙක් නැත්නම් මුල්ම ID එක ලෙස S1001 ලබා දීම
            txtid.setText("S1001");
        }
        
        // User ට මේ ID එක manually වෙනස් කරන්න බැරි වෙන්න lock කිරීම (UI Best Practice)
        txtid.setEditable(false); 
        
    } catch (java.sql.SQLException e) {
        System.out.println("Auto ID Error: " + e.getMessage());
    }
}
    public void tb_load() {
    try {
        java.sql.Connection con = db.mycon();
        java.sql.Statement s = con.createStatement();
        java.sql.ResultSet rs = s.executeQuery("SELECT * FROM suppliers");
        
        // Table model එක ලබා ගැනීම
        javax.swing.table.DefaultTableModel dt = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0); // කලින් තිබුන row අයින් කිරීම
        
        while (rs.next()) {
            java.util.Vector v = new java.util.Vector();
            v.add(rs.getString("supplier_id"));
            v.add(rs.getString("company_name"));
            v.add(rs.getString("phone"));
            v.add(rs.getString("email"));
            
            dt.addRow(v);
        }
    } catch (java.sql.SQLException e) {
        System.out.println(e.getMessage());
    }
    }

